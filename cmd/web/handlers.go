package main

import (
	"errors"
	"fmt"
	"kazybek.net/finalproject/pkg/forms"
	"kazybek.net/finalproject/pkg/models"
	"net/http"
	"strconv"
)

func (app *application) home(w http.ResponseWriter, r *http.Request) {
	s, err := app.jazbalar.Latest()
	if err != nil {
		app.serverError(w, err)
		return
	}

	app.render(w, r, "home.page.tmpl", &templateData{
		Jazbalar: s,
	})
}

func (app *application) showJazba(w http.ResponseWriter, r *http.Request) {
	id, err := strconv.Atoi(r.URL.Query().Get(":id"))
	if err != nil || id < 1 {
		app.notFound(w)
		return
	}
	s, err := app.jazbalar.Get(id)
	if err != nil {
		if errors.Is(err, models.ErrNoRecord) {
			app.notFound(w)
		} else {
			app.serverError(w, err)
		}
		return
	}
	app.render(w, r, "show.page.tmpl", &templateData{
		Jazba: s,
	})

}

func (app *application) createJazba(w http.ResponseWriter, r *http.Request) {
	err := r.ParseForm()
	if err != nil {
		app.clientError(w, http.StatusBadRequest)
		return
	}
	form := forms.New(r.PostForm)
	form.Required("takyryp", "mazmuny", "expires")
	form.MaxLength("takyryp", 100)
	form.PermittedValues("expires", "365", "7", "1")
	if !form.Valid() {
		app.render(w, r, "create.page.tmpl", &templateData{Form: form})
		return
	}
	id, err := app.jazbalar.Insert(form.Get("takyryp"), form.Get("mazmuny"), form.Get("expires"))
	if err != nil {
		app.serverError(w, err)
		return
	}

	app.session.Put(r, "flash", "Jazba kuraldı!")
	http.Redirect(w, r, fmt.Sprintf("/jazba/%d", id), http.StatusSeeOther)
}

